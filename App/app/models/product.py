        self.codigo = codigo
        self.name = name
        self.price = price
        self.productImage = productImage
        self.marca = marca
        self.pesoLiq = pesoLiq
        self.pesoBruto = pesoBruto
        self.gtin = gtin
        self.gtinEmbalagem = gtinEmbalagem
        self.larguraProduto = larguraProduto
        self.alturaProduto = alturaProduto
        self.profundidadeProduto = profundidadeProduto

